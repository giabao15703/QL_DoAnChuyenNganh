                                        something = Smb don’t need to do something</span></span></strong></span></p>

                    <p><em><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Ý nghĩa: không cần thiết phải làm
                                        gì…</span></span></span></em></p>

                    <p><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                    style="font-size:16px">Ví dụ:<strong> It is not necessary for you to do this
                                        exercise.</strong> (Bạn không cần thiết phải làm bài tập
                                    này)</span></span></span></p>

                    <p><span style="color:#1abc9c"><strong><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Cấu trúc 26. To look forward to
                                        V-ing</span></span></strong></span></p>

                    <p><em><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Ý nghĩa: mong chờ, mong đợi làm
                                        gì…</span></span></span></em></p>

                    <p><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                    style="font-size:16px">Ví dụ: <strong>We are looking forward to going on
                                        holiday.</strong> (Chúng tôi mong đến kỳ nghỉ)</span></span></span></p>

                    <p><span style="color:#1abc9c"><strong><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Cấu trúc 27. To provide smb from
                                        V-ing</span></span></strong></span></p>

                    <p><em><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Ý nghĩa: cung cấp cho ai cái
                                        gì…</span></span></span></em></p>

                    <p><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                    style="font-size:16px">Ví dụ:<strong> Can you provide us with some books in history?
                                    </strong>(Bạn có thể đưa cho chúng tôi một số cuốn sách lịch sử được
                                    không?)</span></span></span></p>

                    <p><span style="color:#1abc9c"><strong><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Cấu trúc 28. To prevent someone from
                                        V-ing</span></span></strong></span></p>

                    <p><em><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Ý nghĩa: cản trở ai làm gì…</span></span></span></em></p>

                    <p><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                    style="font-size:16px">Ví dụ: <strong>The rain stopped us from going for a
                                        walk.</strong> (Trời mưa khiến chúng tôi không thể tản bộ)</span></span></span>
                    </p>

                    <p><span style="color:#1abc9c"><strong><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Cấu trúc 29. To fail to do
                                        something</span></span></strong></span></p>

                    <p><em><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Ý nghĩa: không làm được cái gì… /thất bại trong việc làm
                                        cái gì…</span></span></span></em></p>

                    <p><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                    style="font-size:16px">Ví dụ: <strong>We failed to do this exercise. </strong>(Chúng
                                    tôi không làm được bài tập này)</span></span></span></p>

                    <p><span style="color:#1abc9c"><strong><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Cấu trúc 30. To be succeed in
                                        V-ing</span></span></strong></span></p>

                    <p><em><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Ý nghĩa: thành công trong việc làm cái
                                        gì…</span></span></span></em></p>

                    <p><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                    style="font-size:16px">Ví dụ: <strong>We were succeed in passing the exam.</strong>
                                    (Chúng tôi đã vượt qua kỳ thi thành công)</span></span></span></p>

                    <p><span style="color:#1abc9c"><strong><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Cấu trúc 31. It is (very) kind of someone to do
                                        something</span></span></strong></span></p>

                    <p><em><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Ý nghĩa: ai thật tốt bụng/tử tế khi làm
                                        gì…</span></span></span></em></p>

                    <p><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                    style="font-size:16px">Ví dụ: <strong>It is very kind of you to help me.</strong>
                                    (Bạn thật tử tế khi giúp đỡ tôi)</span></span></span></p>

                    <p><span style="color:#1abc9c"><strong><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Cấu trúc </span></span></strong></span><strong><span
                                style="color:#1abc9c"><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">32. To have no idea of something = Don’t know about
                                        something</span></span></span></strong></p>

                    <p><em><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Ý nghĩa: không biết/ không có ý tưởng về cái
                                        gì…</span></span></span></em></p>

                    <p><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                    style="font-size:16px">Ví dụ: <strong>I have no idea of this word = I don’t know
                                        this word.</strong> (Tôi không biết từ này)</span></span></span></p>

                    <p><span style="color:#1abc9c"><strong><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Cấu trúc 33. To advise someone to do
                                        something</span></span></strong></span></p>

                    <p><em><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                        style="font-size:16px">Ý nghĩa: khuyên ai làm gì…</span></span></span></em></p>

                    <p><span style="color:#000000"><span style="font-family:Times New Roman,Times,serif"><span
                                    style="font-size:16px">Ví dụ:<strong> Our teacher advises us to study hard.</strong>
                                    (Giáo viên khuyên chúng tôi học hành chăm chỉ)</span></span></span></p>

